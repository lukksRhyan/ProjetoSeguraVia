CREATE TABLE if not exists acidentes (
    id VARCHAR(10),
    data_inversa DATE,
    dia_semana VARCHAR(20),
    horario TIME,
    uf VARCHAR(2),
    br VARCHAR(10),
    km VARCHAR(10),
    municipio VARCHAR(50),
    causa_acidente VARCHAR(100),
    tipo_acidente VARCHAR(50),
    classificacao_acidente VARCHAR(50),
    fase_dia VARCHAR(20),
    sentido_via VARCHAR(20),
    condicao_metereologica VARCHAR(20),
    tipo_pista VARCHAR(20),
    tracado_via VARCHAR(50),
    uso_solo VARCHAR(3),
    pessoas INT,
    mortos INT,
    feridos_leves INT,
    feridos_graves INT,
    ilesos INT,
    ignorados INT,
    feridos INT,
    veiculos INT,
    latitude FLOAT,
    longitude FLOAT,
    regional VARCHAR(20),
    delegacia VARCHAR(10)
);
